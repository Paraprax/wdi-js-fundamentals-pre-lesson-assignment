ghosts = ['Inky', 'Blinky', 'Pinky', 'Clyde'];


//for variable index = 0, when index is smaller than the # of ghosts in the array, increment the index by 1. Pac-Man eats each ghost in the index
for (var index = 0; index < ghosts.length; index++) {
  console.log('Pac-Man eats ' + ghosts[index]);
}

