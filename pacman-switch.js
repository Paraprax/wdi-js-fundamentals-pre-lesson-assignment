var ghost1 = 'Inky';
var ghost2 = 'Blinky';
var ghost3 = 'Pinky';
var ghost4 = 'Clyde';
var colour;

switch (ghost1, ghost2, ghost3, ghost4) {
  case 'Inky':
    colour = 'Cyan';
    break;
  case 'Blinky':
    colour = 'Red';
    break;
  case 'Pinky':
    colour = 'Pink';
    break;
  case 'Clyde':
    colour = 'Orange';
    break;

}

console.log(ghost1 + ' is the colour ' + colour);
console.log(ghost2 + ' is the colour ' + colour);
console.log(ghost3 + ' is the colour ' + colour);
console.log(ghost4 + ' is the colour ' + colour);
